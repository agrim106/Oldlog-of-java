public class dowhileloop {
    public static void main(String[] args) {
        /*
         * In this type of loop we first write the output or the print statement and
         * then add the condition for the statement to produce our output or not
         */
        int counter = 1;

        do {
            System.out.println("Print this line ");
            counter++;
        } while (counter <= 10);

    }

}
