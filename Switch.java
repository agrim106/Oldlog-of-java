public class Switch {
    public static void main(String[] args) {
        int number = 0;
        switch (number) {
            case 1:
                System.out.println("Bru");
                break;
            case 2:
                System.out.println("Man");
                break;
            case 3:
                System.out.println("dam");
                break;
            case 4:
                System.out.println("bee");
                break;
            default:
                System.out.println("bozo");
        }
    }
}
