public class datatypes {
    public static void main(String args[]) {

        // its important to remember that the datatypes in java are all written in small
        // letters.
        byte b = 23;
        System.out.println(b);
        char cb = 's';
        System.out.println(cb);
        int n = 909;
        System.out.println(n);
        boolean var = false;
        System.out.println(var);
        // the others are long and double i will not be writing those .
        double price = 50.1;
        System.out.println(price);
        //pro tip remember the ranges of data types.
    }
}