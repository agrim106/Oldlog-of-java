public class Relationalops {
    public static void main(String[] args) {
        /* We all know about relational operators, lets see some of them implemented */
        int q = 12;
        int e = 32;
        System.out.println(q == e);

        int d = 10;
        int f = 90;
        System.out.println(d != f);

    }

}
